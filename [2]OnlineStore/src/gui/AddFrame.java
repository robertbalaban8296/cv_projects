    public void addActionListeners(DepartmentType d) {
        jButton1.addActionListener(ev -> {
            Item i = new Item();
            if (jTextField1.getText().equals("") || jTextField2.equals("")) {
                JOptionPane.showMessageDialog(null, "Nume sau pret invalid");
            } else {
                i.setNume(jTextField1.getText());
                i.setPret(Double.parseDouble(jTextField2.getText()));
                MainService.getMainService().adaugaItem(i, d);
                dispose();
            }
        }       
        );
        jButton2.addActionListener(ev -> dispose());
    }
